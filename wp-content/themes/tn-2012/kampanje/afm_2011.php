<h2><a href="http://studentersamfundet.no/vis.php?ID=4553">Lørdag 9. april: Alle Festers mor!</a></h2>
<img src="http://studentersamfundet.no/imageResize.php?pic=bilder/program/4553.jpg&maxwidth=300" style="float:left;margin:4px;" />
<p>

Oslos desidert største og utvilsomt mest legendariske studentfest er tilbake. 9. april går byens studentforeninger sammen for å gi deg en hjemme alene-fest du sent vil glemme. 
</p>

<p>
Alle festers mor ble født på 90-tallet og var i mange år det sikreste vårtegn for byens studenter. I fjor kom hun tilbake etter en lengre skjønnhetssøvn og i år er hun tilbake for fullt på studenthuset Chateau Neuf, der hun fyller alle lokaler med forskjellige barer fra lounge, via BokCaféen, gjennom 60-tallsparty til svett klubbstemning. 
</p>
<p>
20 foreninger, 16 barer, 6 DJs, deriblandt: SKATEBÅRD, Dj Thomas Urv og Tobii
